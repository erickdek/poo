//OPERADORES RELACIONALES
package poo_p1_pract3_operadores_relacionales;
public class POO_P1_PRACT3_OPERADORES_RELACIONALES {
    public static void main(String[] args) {
        int val1 = 2;
        int val2 = 3;
        System.out.println("IGUAL" + val1 == val2);
        System.out.println(val1 != val2);
        System.out.println(val1 > val2);
        System.out.println(val1 < val2);
        System.out.println(val1 >= val2);
        System.out.println(val1 <= val2);
    }
}
